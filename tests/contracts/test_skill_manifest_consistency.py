@pytest.mark.parametrize("pipeline_path", all_pipelines(), ids=lambda p: p.stem)
def test_manifest_stage_tools_are_registered_and_callable(pipeline_path: Path) -> None:
    """Every stage tool reference must be registered and callable from tools_available."""
    registry.discover()
    known_tools = set(registry._tools)
    manifest = load_pipeline(pipeline_path)
    failures: list[str] = []

    for stage in manifest.get("stages", []):
        stage_name = stage.get("name", "<unnamed>")
        declared = _declared_stage_tools(stage)
        available = set(declared["tools_available"])

        for field, tools in declared.items():
            for tool_name in tools:
                if tool_name not in known_tools:
                    failures.append(f"{stage_name}: {field} references unknown tool {tool_name!r}")

        for field in ("required_tools", "optional_tools"):
            for tool_name in declared[field]:
                if tool_name not in available:
                    failures.append(
                        f"{stage_name}: {field} includes {tool_name!r} but tools_available omits it"
                    )

        for sub_stage in stage.get("sub_stages") or []:
            sub_name = sub_stage.get("name", "<unnamed>")
            for tool_name in sub_stage.get("tools_available") or []:
                if tool_name not in known_tools:
                    failures.append(
                        f"{stage_name}.{sub_name}: tools_available references unknown tool {tool_name!r}"
                    )

    for mode in manifest.get("production_modes") or []:
        mode_name = mode.get("name", "<unnamed>")
        for field in ("required_tools", "optional_tools"):
            for tool_name in mode.get(field) or []:
                if tool_name not in known_tools:
                    failures.append(
                        f"production_mode {mode_name}: {field} references unknown tool {tool_name!r}"
                    )

    reference_input = manifest.get("reference_input") or {}
    for tool_name in reference_input.get("analysis_tools") or []:
        if tool_name not in known_tools:
            failures.append(f"reference_input: analysis_tools references unknown tool {tool_name!r}")

    assert not failures, (
        f"Pipeline {pipeline_path.stem!r} has invalid tool declarations:\n  - "
        + "\n  - ".join(failures)
    )


