"""Contract linter: skill ↔ manifest ↔ schema ↔ tool consistency.

Catches the four classes of drift this codebase keeps regenerating:

  1. Artifact graph closure — every artifact consumed via `required_artifacts_in`
     must be produced by another stage in the same manifest.
  2. Schema coverage — every artifact that crosses a stage boundary must have
     a schema file on disk.
  3. Skill ↔ schema field references — when a skill mentions
     `<artifact>.<field.path>`, that path must resolve in the artifact schema
     (unless the schema sets additionalProperties: true at that level).
  4. Embedded JSON examples — fenced ```json blocks in skills that are
     explicitly tagged as a known artifact must validate against that schema.

The linter is intentionally narrow: it asserts only what is mechanically
checkable without false positives. Everything else stays in human review.
"""

from __future__ import annotations

import json
import re
from collections import defaultdict
from pathlib import Path

import jsonschema
import pytest
import yaml

from tools.tool_registry import registry

REPO = Path(__file__).resolve().parents[2]
PIPELINE_DIR = REPO / "pipeline_defs"
SCHEMA_DIR = REPO / "schemas" / "artifacts"
SKILLS_DIR = REPO / "skills"


# ---------------------------------------------------------------------------
# Loaders
# ---------------------------------------------------------------------------


def all_pipelines() -> list[Path]:
    return sorted(PIPELINE_DIR.glob("*.yaml"))


def load_pipeline(path: Path) -> dict:
    with open(path) as f:
        return yaml.safe_load(f)


def known_artifact_names() -> set[str]:
    return {p.stem.replace(".schema", "") for p in SCHEMA_DIR.glob("*.schema.json")}


def load_schema(name: str) -> dict | None:
    path = SCHEMA_DIR / f"{name}.schema.json"
    if not path.exists():
        return None
    return json.loads(path.read_text())


def collect_artifacts(manifest: dict) -> tuple[set[str], set[str]]:
    producers: set[str] = set()
    consumers: set[str] = set()
    for stage in manifest.get("stages", []):
        for unit in [stage, *(stage.get("sub_stages") or [])]:
            for a in unit.get("produces") or []:
                producers.add(a)
            for a in unit.get("required_artifacts_in") or []:
                consumers.add(a)
            for a in unit.get("optional_artifacts_in") or []:
                consumers.add(a)
    return producers, consumers


def _declared_stage_tools(stage: dict) -> dict[str, list[str]]:
    return {
        "required_tools": stage.get("required_tools") or [],
        "optional_tools": stage.get("optional_tools") or [],
        "fallback_tools": stage.get("fallback_tools") or [],
        "tools_available": stage.get("tools_available") or [],
    }


# ---------------------------------------------------------------------------
# Check 1: artifact graph closure
# ---------------------------------------------------------------------------

# Artifacts legitimately produced outside the pipeline stage graph.
# These appear in `required_artifacts_in` / `optional_artifacts_in` without
# a corresponding `produces:` declaration because they're external inputs
# generated by a meta-skill or utility before the pipeline starts. Each
# entry maps the artifact name to a one-line rationale so the exemption
# stays auditable.
EXTERNAL_INPUT_ARTIFACTS: dict[str, str] = {
    # Produced by lib/source_media_review.review_source_media() (a utility,
    # not a stage) when the user supplies media files. See skills/meta/
    # video-reference-analyst.md and skills/meta/reviewer.md:255-266.
    "source_media_review": "produced by lib/source_media_review utility before first planning stage",
    # The user's original natural-language brief — pre-pipeline input,
    # not a stage output. Distinct from research_brief (which IS a stage output).
    "brief": "user-supplied natural-language brief — pre-pipeline input",
    # Produced by the video-reference analysis workflow when the user provides
    # a reference video before normal pipeline planning begins.
    "video_analysis_brief": "produced by the video-reference analysis workflow before pipeline planning",
}


@pytest.mark.parametrize("pipeline_path", all_pipelines(), ids=lambda p: p.stem)
def test_artifact_graph_is_closed(pipeline_path: Path) -> None:
    """Every consumed artifact must be produced by some stage OR be an external input."""
    manifest = load_pipeline(pipeline_path)
    producers, consumers = collect_artifacts(manifest)
    orphans = consumers - producers - EXTERNAL_INPUT_ARTIFACTS.keys()
    assert not orphans, (
        f"Pipeline {pipeline_path.stem!r} consumes artifacts with no producer: "
        f"{sorted(orphans)}.\n"
        f"Either add a stage that produces them, remove them from "
        f"required_artifacts_in / optional_artifacts_in, or document them as "
        f"external inputs in EXTERNAL_INPUT_ARTIFACTS with a rationale."
    )


# ---------------------------------------------------------------------------
# Check 2: schema coverage for cross-stage artifacts
# ---------------------------------------------------------------------------

# Artifacts that intentionally have no schema (e.g., free-form intermediate
# state). Add to this set with a code comment explaining why, so the exemption
# stays auditable. Empty by default — every cross-stage artifact should have
# a schema unless explicitly justified.
SCHEMALESS_ARTIFACTS_ALLOWED: set[str] = set()


@pytest.mark.parametrize("pipeline_path", all_pipelines(), ids=lambda p: p.stem)
def test_cross_stage_artifacts_have_schemas(pipeline_path: Path) -> None:
    """If an artifact crosses a stage boundary, its schema must exist on disk."""
    manifest = load_pipeline(pipeline_path)
    producers, consumers = collect_artifacts(manifest)
    cross_stage = producers & consumers
    schemas = known_artifact_names()
    missing = {
        name
        for name in cross_stage
        if name not in schemas and name not in SCHEMALESS_ARTIFACTS_ALLOWED
    }
    assert not missing, (
        f"Pipeline {pipeline_path.stem!r} crosses stages with artifacts that have "
        f"no schema in schemas/artifacts/: {sorted(missing)}.\n"
        f"Add a <name>.schema.json file or document the exemption in "
        f"SCHEMALESS_ARTIFACTS_ALLOWED."
    )


